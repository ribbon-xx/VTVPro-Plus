package mdn.vtvpluspro.object;

public class StreamInfo {
	private String id;
	private String url;
	
	public StreamInfo() {
		this.id = "";
		this.url = "";
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
}
