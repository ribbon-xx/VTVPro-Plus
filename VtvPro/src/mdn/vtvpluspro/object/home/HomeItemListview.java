package mdn.vtvpluspro.object.home;

import mdn.vtvpluspro.object.ItemVtvPlusInfo;

public class HomeItemListview extends HomeObject {
	private ItemVtvPlusInfo itemLeft;
	private ItemVtvPlusInfo itemRight;
	
	public HomeItemListview() {
		setIsCategory(false);
		itemLeft = null;
		itemRight = null;
	}

	public ItemVtvPlusInfo getItemLeft() {
		return itemLeft;
	}

	public void setItemLeft(ItemVtvPlusInfo itemLeft) {
		this.itemLeft = itemLeft;
	}

	public ItemVtvPlusInfo getItemRight() {
		return itemRight;
	}

	public void setItemRight(ItemVtvPlusInfo itemRight) {
		this.itemRight = itemRight;
	}
	
	
}
